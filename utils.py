import re  # استيراد مكتبة regex (التعبيرات النمطية) لمعالجة النصوص

def extract_code_quality_score(result):
    # تعريف دالة تستخرج درجة جودة الكود من نص معين
    
    # استخدام تعبير نمطي للبحث عن النص الذي يحتوي على "Code Quality Score: X/10"
    match = re.search(r"Code Quality Score:\s*(\d+)/10", result)
    # التفسير:
    # r"Code Quality Score:\s*(\d+)/10" هو تعبير نمطي يعني:
    # - "Code Quality Score:" هو نص ثابت يجب أن يكون موجودًا
    # - "\s*" يعني وجود أي عدد من المسافات (بما في ذلك عدم وجودها)
    # - "(\d+)" يعني مجموعة تحتوي على رقم واحد أو أكثر (X)
    # - "/10" هو نص ثابت يتبع الرقم
    
    if match:  # التحقق مما إذا تم العثور على تطابق
        # إذا تم العثور على تطابق، استخرج الرقم من المجموعة الأولى في التعبير النمطي
        return int(match.group(1))  # تحويل النص المستخرج (الرقم) إلى عدد صحيح
    return None  # إذا لم يتم العثور على تطابق، إرجاع None
